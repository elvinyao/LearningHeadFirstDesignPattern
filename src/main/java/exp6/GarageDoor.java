package exp6;

public class GarageDoor {
    public void up() {
        System.out.println("Door Up");
    }

    public void down() {
        System.out.println("Door down");
    }

    public void stop() {
        System.out.println("Door stop");
    }

    public void lightOn() {
        System.out.println("Door lightOn");
    }

    public void lightOff() {
        System.out.println("Door lightOff");
    }
}
